function verificarDivisibilidade(numero) {
    if (numero % 2 === 0) {
      console.log(numero + " é divisível por 2.");
    }
    if (numero % 3 === 0) {
      console.log(numero + " é divisível por 3.");
    }
    if (numero % 5 === 0) {
      console.log(numero + " é divisível por 5.");
    }
  }
  

  
  verificarDivisibilidade(10);  
  verificarDivisibilidade(15);  
  verificarDivisibilidade(7);  
  