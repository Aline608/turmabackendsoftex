function classificarNumero(numero) {
    if (numero < 0) {
      console.log("O número é negativo.");
    } else if (numero === 0) {
      console.log("O número é zero.");
    } else {
      console.log("O número é positivo.");
    }
  }
  
  
  
  classificarNumero(-6);   
  classificarNumero(0);    
  classificarNumero(10); 
  